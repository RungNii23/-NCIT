            <h1>อุทยานฯ หาดขนอม-หมู่เกาะทะเลใต้</h1>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;อุทยานฯ หาดขนอม-หมู่เกาะทะเลใต้ มีพื้นที่ทางบกเป็นแนวเทือกเขาน้อยใหญ่สลับซับซ้อนทอดตัวจากทิศเหนือสู่ทิศใต้
              มีพื้นที่ราบผืนใหญ่เชิงภูเขาเปิดสู่ฝั่งทะเลด้านอ่าวไทย มียอดเขาหลวงเป็นยอดเขาสูงสุดประมาณ
              814 เมตรจากระดับน้ำทะเล มีเกาะสำคัญมากมายถึง 11 เกาะ ทั้งเกาะแตน เกาะราบ
              เกาะมัดโกง เกาะมัดแตง เกาะวังนอก เกาะวังใน เกาะมัดสุ่ม เกาะราใหญ่ เกาะท่าไร่
              เกาะผี และเกาะน้อย สภาพป่าบนพื้นดินและบนเกาะของอุทยานฯ หาดขนอม-หมู่เกาะทะเลใต้
              มีทั้งป่าดิบชื้น ป่าดิบแล้ง ป่าชายเลน และป่าเขาหินปูน ความงามของอุทยานฯ
              ทางทะเลแห่งนี้ คงหนีไม่พ้นธรรมชาติทางท้องทะเลสวยงามบริเวณเกาะต่างๆ ในพื้นที่ของอุทยานฯ
              ที่แต่ละเกาะล้วนมีชายหาดทรายขาวสวย เป็นเวิ้งอ่าวให้คุณได้ขึ้นไปนอนอาบแดด
              ทั้งอ่าวท้องหยี อ่าวท้องยาง อ่าวคอเขา อ่าวหน้าด่าน อ่าวแฝงเภา อ่าวท้องชิง
              อ่าวในเพลา และยังมีปะการังใต้น้ำอุดมสมบูรณ์ทั้งปะการังจาน ปะการังสมอง และปะการังเขากวางให้ดำชมกันอย่างเพลิดเพลินด้วย
              หากคุณไม่อยากนั่งเรือข้ามเกาะห่างจากอำเภอขนอมเพียง 1.5 กิโลเมตร ก็มีหาดขนอม
              ชายหาดสวยที่ทอดยาวไปเชื่อมกับ หาดในเปร็ด หาดในเพลา และหาดปากนํ้า เรียกรวมกันว่า
              หาดหน้าด่าน เป็นหาดทรายยาวสลับโขดหิน และสามารถเล่นน้ำได้สบายทุกหาด</p>
