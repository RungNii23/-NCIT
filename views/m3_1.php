            <h1>วัดพระมหาธาตุวรมหาวิหาร นครศรีธรรมราช</h1>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;พระบรมธาตุเจดีย์ เป็นเจดีย์สถาปัตยกรรมแบบทรงระฆังคว่ำ มีจุดเด่นที่ยอดเจดีย์
              ซึ่งหุ้มด้วยทองคำแท้ จากความเชื่อ เล่าสืบตอบกันมาว่า องค์พระธาตุประกอบด้วยทองรูปพรรณและของมีค่ามากมายจรดปลายเจดีย์
              ซึ่งสิ่งของมีค่า เหล่านี้พุทธศสานิกชนนำมาถวาย แด่องค์พระสัมมาสัมพุทธเจ้าเพื่อให้ตนได้พบกับนิพพาน
              จากคำขวัญประจำ จังหวัดเมืองประวัติศาสตร์ พระธาตุทองคำ ชื่นฉ่ำธรรมชาติ แร่ธาตุอุดม
              เครื่องถมสามกษัตริย์ มากวัดมากศิลป์ ครบสิ้นกุ้งปูข้อความว่า พระธาตุทองคำ
              จึงหมายถึง ยอดเจดีย์ทองของพระบรมธาตุ นั่นเอง และหากใครต้องการ ชมยอดพระธาตสีทองเหลืองอร่ามอย่างใกล้ชิด
              มีบริการกล้องส่องทางไกลให้ใช้บริการสนนราคาแล้ว แต่ตกลง กันว่าจะชื่นชมความงดงามนั้นนานเพียงใด
              ด้วยความมีชื่อเสียงและศักดิ์สิทธิ์ของพระบรมธาตุเจดีย์ ดึงดูดให้ผู้คน จากทั่วสารทิศแวะมากราบไหว้ขอพร</p>
